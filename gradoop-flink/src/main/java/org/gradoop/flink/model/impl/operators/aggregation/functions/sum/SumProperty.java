package org.gradoop.flink.model.impl.operators.aggregation.functions.sum;

import org.gradoop.common.model.impl.pojo.Element;
import org.gradoop.common.model.impl.properties.PropertyValue;
import org.gradoop.common.model.impl.properties.PropertyValueUtils;
import org.gradoop.flink.model.impl.operators.aggregation.functions.BaseAggregateFunction;

/**
 * Superclass if aggregate functions that sum property values of elements.
 */
public abstract class SumProperty<T extends Element> extends BaseAggregateFunction<T> {

  /**
   * Prefix of the property key generated by {@link SumProperty} aggregate function.
   */
  private static final String PROPERTY_KEY_PREFIX_SUM = "sum_";

  /**
   * Constructor.
   *
   * @param propertyKey property key to aggregate
   */
  public SumProperty(String propertyKey) {
    super(propertyKey);
  }

  @Override
  public PropertyValue aggregate(PropertyValue aggregate, PropertyValue increment) {
    return PropertyValueUtils.Numeric.add(aggregate, increment);
  }

  @Override
  public PropertyValue getIncrement(T element) {
    return element.getPropertyValue(super.getAggregatePropertyKey());
  }

  @Override
  public String getAggregatePropertyKey() {
    return PROPERTY_KEY_PREFIX_SUM + super.getAggregatePropertyKey();
  }
}

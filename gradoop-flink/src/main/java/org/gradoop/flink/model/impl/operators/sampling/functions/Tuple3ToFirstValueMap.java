package org.gradoop.flink.model.impl.operators.sampling.functions;

import org.apache.flink.api.common.functions.MapFunction;
import org.apache.flink.api.java.tuple.Tuple3;
import org.gradoop.common.model.impl.pojo.Edge;
import org.gradoop.common.model.impl.pojo.Vertex;

/**
 * A map from tuple3 of an edge and its vertices to the edge
 */
public class Tuple3ToFirstValueMap implements MapFunction<Tuple3<Edge, Vertex, Vertex>, Edge> {
  /**
   * {@inheritDoc}
   */
  @Override
  public Edge map(Tuple3<Edge, Vertex, Vertex> t3) throws Exception {
    return t3.f0;
  }
}

package org.gradoop.flink.model.impl.operators.sampling;

/**
 * Collected constants for sampling operator
 */
final class SamplingConstants {

  /**
   * Key of a property generated by {@link RandomNonUniformVertexSampling}
   */
  static final String PROPERTY_KEY_MAX_DEGREE = "_maxDegree";

  /**
   * Key of a property generated by {@link RandomVertexNeighborhoodSampling}
   */
  static final String PROPERTY_KEY_SAMPLED = "sampled";


  /**
   The caller references the constants using <tt>SamplingConstants.PROPERTY_KEY_MAX_DEGREE</tt>,
   and so on. Thus, the caller should be prevented from constructing objects of
   this class, by declaring this private constructor.
   */
  private SamplingConstants() {
    // this prevents even the native class from calling this constructor
    throw new AssertionError();
  }
}
